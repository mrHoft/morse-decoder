const MORSE_TABLE = {
  ".-": "a",
  "-...": "b",
  "-.-.": "c",
  "-..": "d",
  ".": "e",
  "..-.": "f",
  "--.": "g",
  "....": "h",
  "..": "i",
  ".---": "j",
  "-.-": "k",
  ".-..": "l",
  "--": "m",
  "-.": "n",
  "---": "o",
  ".--.": "p",
  "--.-": "q",
  ".-.": "r",
  "...": "s",
  "-": "t",
  "..-": "u",
  "...-": "v",
  ".--": "w",
  "-..-": "x",
  "-.--": "y",
  "--..": "z",
  ".----": "1",
  "..---": "2",
  "...--": "3",
  "....-": "4",
  ".....": "5",
  "-....": "6",
  "--...": "7",
  "---..": "8",
  "----.": "9",
  "-----": "0",
};

const CODES = { "00": "", 10: ".", 11: "-" };

function decode(expr) {
  return expr
    .split("**********")
    .map((word) =>
      word
        .match(/\d{10}/g)
        .map(
          (symbol) =>
            MORSE_TABLE[
              symbol
                .match(/\d{2}/g)
                .map((code) => CODES[code])
                .join("")
            ]
        )
        .join("")
    )
    .join(" ");
}

module.exports = {
  decode,
};
